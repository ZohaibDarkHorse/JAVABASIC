package condtionalStatement;

public class switchStatement {

	public static void main(String[] args) {
		
		int num=500;
		
		switch(num) {
		
		case 100:System.out.println("Case1");
		break;
		
		case 200:System.out.println("Case2");
		break;
		
		case 500:System.out.println("Case3");
		break;
		
		case 600:System.out.println("Case4");
		break;
		}
		
	}
}
